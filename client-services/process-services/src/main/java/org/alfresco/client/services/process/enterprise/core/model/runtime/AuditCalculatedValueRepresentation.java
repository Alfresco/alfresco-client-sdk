package org.alfresco.client.services.process.enterprise.core.model.runtime;

/**
 * @author Vasile Dirla
 */
public class AuditCalculatedValueRepresentation
{

    protected String name;

    protected String value;

    public AuditCalculatedValueRepresentation()
    {

    }

    public AuditCalculatedValueRepresentation(String name, String value)
    {
        this.name = name;
        this.value = value;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getValue()
    {
        return value;
    }

    public void setValue(String value)
    {
        this.value = value;
    }

}
